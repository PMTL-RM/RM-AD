package com.so2.running.Fragment;

public class NotificationListItem
{

    private String content;
    private String friend_name;
    private String ViewType;
    private String groupname;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {this.content = content;}


    public String getFriend_name() {
        return friend_name;
    }

    public void setFriend_name(String friend_name) {this.friend_name = friend_name;}


    public String getType() {return ViewType;}

    public void setType(String ViewType) {this.ViewType = ViewType;}


    public String getGroupName() {return groupname;}

    public void setGroupName(String groupname) {this.groupname = groupname;}

}